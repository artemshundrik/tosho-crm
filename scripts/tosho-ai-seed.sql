select
  workspace_targets.workspace_id,
  knowledge_seed.title,
  knowledge_seed.slug,
  knowledge_seed.summary,
  knowledge_seed.body,
  knowledge_seed.tags,
  knowledge_seed.keywords,
  'active',
  knowledge_seed.source_label,
  knowledge_seed.source_href
from workspace_targets
cross join knowledge_seed
where not exists (
  select 1
  from tosho.support_knowledge_items existing
  where existing.workspace_id = workspace_targets.workspace_id
    and existing.slug = knowledge_seed.slug
);
